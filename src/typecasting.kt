fun main(args: Array<String>) {

    //double to int type casting

    println("4.554 to int is: " +(4.554.toInt() ))

    //int to char type casting

    println("65 to char is: " + (65.toChar()))

    //char to int type casting

    println("B to int is: " + ('B'.toInt()))

    var num = 45.8988
    var num2 = num.toInt()

    println(num2)

}